class Calculadora:
    def suma(self, primer_numero, segundo_numero):
        return primer_numero + segundo_numero
    
    def resta(self, primer_numero, segundo_numero):
        return primer_numero - segundo_numero

    def multiplicacion(self, primer_numero, segundo_numero):
        return primer_numero * segundo_numero

    def division(self, primer_numero, segundo_numero):
        return primer_numero / segundo_numero
